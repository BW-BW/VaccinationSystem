    private void btnVCenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVCenActionPerformed

        DefaultTableModel table = (DefaultTableModel)jTable1.getModel();
        while(table.getRowCount() > 0)
        {
            table.removeRow(0);
        }
        String Center = cmbCenter.getSelectedItem().toString();

        Vaccine viewCenter = new Vaccine(Center); //Create Object
        viewCenter.viewVaccine(jTable1, 0, Center); //Call Method

    }//GEN-LAST:event_btnVCenActionPerformed

