    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        DefaultTableModel table = (DefaultTableModel)jTable1.getModel();
        while(table.getRowCount() > 0)
        {
            table.removeRow(0);
        }

        String Keyword = txtCenter.getText();
        String Keyword2 = txtType.getText();
        
        if (Keyword.isBlank() || Keyword2.isBlank()){
            JOptionPane.showMessageDialog(null, "Please enter both vaccine and type");
        }
        else{
            Vaccine Search = new Vaccine();
            Search.Search(jTable1, 0, Keyword, 1, Keyword2);
        }

        
    }//GEN-LAST:event_btnSearchActionPerformed

